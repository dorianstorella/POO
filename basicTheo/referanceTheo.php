<h2>classe definition</h2>
<p>les <strong>classe</strong> contienne la <strong>definition des objets</strong></p>
<p><strong>l'objet </strong>est <strong>creer</strong>  a partir de la <strong>classe</strong></p>
<p>la classe regroupe un ensemble de <strong>variable</strong> et de <strong>fonction</strong> que tout objet possede</p>
<p>les variable sont appellée <strong>propriete ou attribut</strong> et les fonction des <strong>methode</strong></p>

