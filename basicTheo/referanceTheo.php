<h2>en resume</h2>
<ul>
<li>Une classe, c'est un ensemble de variables et de fonctions (attributs et méthodes).</li>
<li>Un objet, c'est une instance de la classe pour pouvoir l'utiliser.</li>
<li>Tous vos attributs doivent être privés. Pour les méthodes, peu importe leur visibilité. C'est ce qu'on appelle le principe d'encapsulation.</li>
<li>On déclare une classe avec le mot-clé <strong>class</strong> suivi du nom de la classe, et enfin deux accolades ouvrantes et fermantes qui encercleront la liste des attributs et méthodes.</li>
</ul>

