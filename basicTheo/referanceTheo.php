<h2>Objet definition</h2>
<p><strong>Cree</strong> quelque chose ,comme un personnage, un employé , des animaux , des abres , ... <strong>Avec des caracteristique </strong> comme couleur des cheveux ,une force, une localisation. il peux <strong>avoir des capacite </strong> comme frapper, gagner de l expe, ... </p>
